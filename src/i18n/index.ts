const MESSAGE_TABLE: Record<ResolvedAppLocale, Record<string, MessageValue>> = {
    'en-US': EN_MESSAGES,
    'zh-CN': ZH_MESSAGES,
}

function formatMessage(template: string, values?: Record<string, string | number>) {
    if (!values) return template
    return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? ''))
}

export function tForLocale(
    locale: AppLocale,
    key: string,
    values?: Record<string, string | number>
): string {
    const resolved = resolveAppLocale(locale)
    const dictionary = MESSAGE_TABLE[resolved] ?? MESSAGE_TABLE['en-US']
    const fallback = MESSAGE_TABLE['en-US'][key] ?? key
    const template = dictionary[key] ?? fallback
    return formatMessage(template, values)
}

export function useI18n() {
    const locale = useEditorStore(s => s.locale)
    const setLocale = useEditorStore(s => s.setLocale)
    const resolvedLocale = resolveAppLocale(locale)

    return {
        locale,
        resolvedLocale,
        setLocale,
        localeOptions: APP_LOCALE_OPTIONS,
        t: (key: string, values?: Record<string, string | number>) => tForLocale(locale, key, values),
    }
}

export { APP_LOCALE_OPTIONS, resolveAppLocale, type AppLocale }
